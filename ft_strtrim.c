#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	size_t	lenght;
	char	*str;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	lenght = end - start;
	str = malloc(sizeof(char) * (lenght + 1));
	if (!str)
		return (NULL);
	str = ft_memcpy(str, s1 + start, lenght);
	str[lenght] = '\0';
	return (str);
}
